package com.manage;

public class Members {
	static int id;
	static String name;
	static String phone;
	public static int getId() {
		return id;
	}
	public void setId(int id) {
		Members.id = id;
	}
	public static String getName() {
		return name;
	}
	public void setName(String name) {
		Members.name = name;
	}
	public static String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		Members.phone = phone;
	}

	
}
