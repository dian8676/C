#include<stdio.h>

#define Num 1000000

int main(void)

{

      char Score;

      printf("Please enter your grades : ");

      scanf("%c",&Score);





      switch(Score)

      {

      case 'A': case 'a':

            printf("장학금은 %d원입니다.\n",Num);

            break;

      case 'B': case 'b':

            printf("The scholarship is %d won.\n",Num*8/10);

            break;

      case 'C': case 'c':

            printf("The scholarship is %d won.\n",Num*6/10);

            break;

      case 'D': case 'd': case 'F': case 'f':

            printf("You can't get a scholarship.\n");

            break;

      default:

            printf("You have entered the wrong grade. Please enter only alphabetical characters.\n");

      }
      

return 0;

}
